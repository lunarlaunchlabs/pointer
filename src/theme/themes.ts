function h(value: string): string {
  return value.startsWith("#") ? value : `#${value}`;
}

function bare(value: string): string {
  return h(value).slice(1);
}

function alpha(hex: string, aa: string): string {
  return `${h(hex)}${aa}`;
}

function resolveTokenPalette(t: TokenPalette): ResolvedTokenPalette {
  const operator = pickDistinctColor(t.operator, [t.keyword, t.function, t.tag], t.fg);
  const delimiter = pickDistinctColor(t.delimiter, [operator, t.keyword, t.tag], t.fg);
  const type = pickDistinctColor(t.type, [t.function, t.number, t.tag, t.keyword], t.fg);
  const parameter = pickDistinctColor(t.parameter, [t.number, type, t.function, t.keyword], t.fg);
  const variable = t.variable ?? parameter;
  const property = pickDistinctColor(t.property, [t.number, t.function, t.attribute, type], t.fg);
  const attribute = pickDistinctColor(t.attribute, [t.number, parameter, type, t.function, t.keyword], t.fg);

  return {
    ...t,
    operator,
    delimiter,
    type,
    parameter,
    property,
    attribute,
    variable,
    builtin: t.constant,
    decorator: attribute,
    macro: t.keyword,
    namespace: type,
    module: t.string,
    label: t.function,
    selector: t.tag,
    configKey: attribute,
    configValue: t.string,
    configSection: type,
    markupHeading: type,
    markupBold: t.fg,
    markupLink: t.function,
    markupQuote: t.comment,
    markupCode: t.string,
    regex: t.escape,
    tagBracket: t.tagBracket ?? pickDistinctColor(t.delimiter, [operator, t.keyword, t.tag], t.fg),
    attributeValue: t.string,
  };
}

function pickDistinctColor(preferred: string, fallbacks: string[], against: string): string {
  const candidates = [preferred, ...fallbacks];
  return candidates.find((candidate) => colorDistance(candidate, against) >= 28) ?? preferred;
}

function colorDistance(a: string, b: string): number {
  const rgbA = hexToRgbChannels(a);
  const rgbB = hexToRgbChannels(b);
  if (!rgbA || !rgbB) return a.toLowerCase() === b.toLowerCase() ? 0 : 255;
  return Math.hypot(rgbA[0] - rgbB[0], rgbA[1] - rgbB[1], rgbA[2] - rgbB[2]);
}

function hexToRgbChannels(value: string): [number, number, number] | null {
  const raw = value.trim().replace(/^#/, "");
  const hex =
    raw.length === 3
      ? raw
          .split("")
          .map((ch) => `${ch}${ch}`)
          .join("")
      : raw.length >= 6
        ? raw.slice(0, 6)
        : null;
  if (!hex || !/^[0-9a-f]{6}$/i.test(hex)) return null;
  return [0, 2, 4].map((index) => parseInt(hex.slice(index, index + 2), 16)) as [
    number,
    number,
    number,
  ];
}

