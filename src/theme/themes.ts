const THEME_BY_ID = new Map(POINTER_THEMES.map((theme) => [theme.id, theme]));

export const DEFAULT_APP_THEME_ID: AppThemeId = "pointer-noir";

export function isAppThemeId(value: string | null | undefined): value is AppThemeId {
  return POINTER_THEME_IDS.includes(value as AppThemeId);
}

export function normalizeAppThemeId(value: string | null | undefined): AppThemeId {
  if (value === "noir") return "pointer-noir";
  if (value === "light") return "pointer-blanc";
  return isAppThemeId(value) ? value : DEFAULT_APP_THEME_ID;
}

export function pointerThemeFor(value: string | null | undefined): PointerTheme {
  return THEME_BY_ID.get(normalizeAppThemeId(value)) ?? POINTER_THEMES[0];
}

export function applyPointerThemeToDocument(value: string | null | undefined): PointerTheme {
  const theme = pointerThemeFor(value);
  if (typeof document === "undefined") return theme;

  const root = document.documentElement;
  for (const [name, cssValue] of Object.entries(theme.css)) {
    root.style.setProperty(name, cssValue);
    const rgb = hexToRgbTriplet(cssValue);
    if (rgb) root.style.setProperty(`${name}-rgb`, rgb);
    else root.style.removeProperty(`${name}-rgb`);
  }
  root.style.colorScheme = theme.type;
  root.dataset.pointerTheme = theme.id;

  const body = document.body;
  body.dataset.pointerTheme = theme.id;
  body.classList.remove("pn-theme-light", "pn-theme-noir");
  for (const id of POINTER_THEME_IDS) {
    body.classList.toggle(`pn-theme-${id}`, id === theme.id);
  }
  return theme;
}

export function themeActionId(themeId: AppThemeId): `theme:${AppThemeId}` {
  return `theme:${themeId}`;
}
